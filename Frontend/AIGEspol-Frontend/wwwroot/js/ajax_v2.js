function soloNumeros(evento) {
    evento = (evento) ? evento : window.event;
    var charCode = (evento.which) ? evento.which : evento.keyCode
    if (charCode > 31 && (charCode < 48 || charCode > 57)) {
        return false
    }
    return true
}



